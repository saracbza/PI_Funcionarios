        jtf_contratacaoFunc.setText("");
        jtf_dependentesFunc.setText("");
        jtf_nascimentoFunc.setText("");   
        jrb_juniorFunc.setSelected(false);
        jrb_plenoFunc.setSelected(false);
        jrb_seniorFunc.setSelected(false);
        jcb_cargo.setSelectedIndex(0);
