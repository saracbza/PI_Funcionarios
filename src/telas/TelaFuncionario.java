    /**
     * Busca os dados no banco para preenchimento de lista
     */
    private void preencherList()
    {
        CargoDAO cargo = new CargoDAO();
        List<Cargo> listaCargo = cargo.listarNome("SELECT nome_cargo FROM cargo ORDER BY cod_cargo");
        
        jcb_cargo.removeAll();
        
        for (Cargo c : listaCargo)
        {
            jcb_cargo.addItem(c);
        }
    }
    
    /**
     * Busca os dados no banco para preenchimento de lista
     */
    private void preencherListEditar()
    {
        CargoDAO cargo = new CargoDAO();
        List<Cargo> listaCargo = cargo.listarNome("SELECT nome_cargo FROM cargo ORDER BY cod_cargo");
        
        jcb_cargoEditar.removeAll();
        
        for (Cargo c : listaCargo)
        {
            jcb_cargoEditar.addItem(c);
        }
        
        FuncionarioDAO dao = new FuncionarioDAO();
        int codCargo = dao.consultaCargoFuncionarioCod(codFunc);
        int cargoIndex = -1;
        for (int i =0; i<=codigos.size(); i++){
            if (codigos.elementAt(i)==codCargo){
                cargoIndex = i;
                break;
            }
        }
        
        jcb_cargoEditar.setSelectedIndex(cargoIndex);
    }
    
   /**
    * Busca os dados no banco para preenchimento de tabela
    */
   private void preencherTab()
    {
        FuncionarioDAO func = new FuncionarioDAO();
        String nomeFunc = jtf_Filtro.getText();
        List<Funcionario> listaFuncionarios = func.pesquisar(nomeFunc);
        
        DefaultTableModel tabelaFuncionarios = (DefaultTableModel) tblFuncionarios.getModel();
        
        tabelaFuncionarios.setNumRows(0);
        for (Funcionario f : listaFuncionarios)
        {
            Object[] obj = new Object[]{
            
            f.getCodigo(), f.getNome(), f.getCpf(), f.getData_nasc(),
            f.getData_contrat(), f.getDependentes()};
            
            tabelaFuncionarios.addRow(obj);
        }

    }
    
