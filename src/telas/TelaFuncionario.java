    private void tblFuncionariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblFuncionariosMouseClicked
        if (tblFuncionarios.getSelectedRow() != -1){
        codFunc = Integer.parseInt(tblFuncionarios.getValueAt(tblFuncionarios.getSelectedRow(), 0).toString());
	jtf_nomeEditar.setText(tblFuncionarios.getValueAt(tblFuncionarios.getSelectedRow(), 1).toString());
	jtf_CPFEditar.setText(tblFuncionarios.getValueAt(tblFuncionarios.getSelectedRow(), 2).toString());
	jtf_nascEditar.setText(tblFuncionarios.getValueAt(tblFuncionarios.getSelectedRow(), 3).toString());
        jtf_contratEditar.setText(tblFuncionarios.getValueAt(tblFuncionarios.getSelectedRow(), 4).toString());
        jtf_depEditar.setText(tblFuncionarios.getValueAt(tblFuncionarios.getSelectedRow(), 5).toString());
}
        FuncionarioDAO dao = new FuncionarioDAO();
        int senioridade = dao.consultaSenFuncionario(codFunc);
        
        switch(senioridade) {
            case 1:
                jrb_juniorEditar.setSelected(true);
                jrb_plenoEditar.setSelected(false);
                jrb_seniorEditar.setSelected(false);
            break;
            case 2:
                jrb_juniorEditar.setSelected(false);
                jrb_plenoEditar.setSelected(true);
                jrb_seniorEditar.setSelected(false);
            break;
            case 3:
                jrb_juniorEditar.setSelected(false);
                jrb_plenoEditar.setSelected(false);
                jrb_seniorEditar.setSelected(true);
            break;
}
      
      preencherListEditar();
    }//GEN-LAST:event_tblFuncionariosMouseClicked

    private void btn_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editarActionPerformed
        String nome, cpf;
	int dep;
        Date data_nasc = new Date();
        Date data_contrat = new Date();
        
        SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-dd"); 
        try {
            data_nasc = formato.parse(jtf_nascEditar.getText());
            data_contrat = formato.parse(jtf_contratEditar.getText());
        } catch (ParseException ex) {
            Logger.getLogger(TelaFuncionario.class.getName()).log(Level.SEVERE, null, ex);
        }
        	
        java.sql.Date nasc = new java.sql.Date(data_nasc.getTime());
        java.sql.Date contrat = new java.sql.Date(data_contrat.getTime());
        
	nome = jtf_nomeEditar.getText();
	cpf = jtf_CPFEditar.getText();
	
	dep = Integer.parseInt(jtf_depEditar.getText());

        
	//salvar informacoes no banco
	Funcionario func = new Funcionario(nome, cpf, nasc, contrat, dep);
	FuncionarioDAO dao = new FuncionarioDAO();
        JOptionPane.showMessageDialog(null, dao.editar(func, codFunc));
        Date hoje = new Date();
        System.out.println(dao.atribuirSenFuncionario(cpf, nivelSenioridade, hoje));
        System.out.println(dao.atribuirCargoFuncionario(cpf, codCargo, hoje));
        
        //limpar caixas de edicao
        limparEditar();
        
        preencherTab();
    }//GEN-LAST:event_btn_editarActionPerformed

