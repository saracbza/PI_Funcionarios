    private void btn_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editarActionPerformed
        String nome;
        float porc;
        
        nome = jtf_nome.getText();
	porc = Float.parseFloat(jtf_porcentagem.getText());
        porc /= 100;
        Senioridade sen = new Senioridade(nome, porc);
        
        SenioridadeDAO dao = new SenioridadeDAO();
        JOptionPane.showMessageDialog(null, dao.editar(sen, codSenioridade));
        
        jtf_nome.setText("");
        jtf_porcentagem.setText("");
        
        preencherTab();
    }//GEN-LAST:event_btn_editarActionPerformed

    /**
     * Busca os dados no banco para preenchimento de tabela
     */
     private void preencherTab()
    {
        SenioridadeDAO sen = new SenioridadeDAO();
        List<Senioridade> listaSen = sen.listar("SELECT * FROM senioridade");
        
        DefaultTableModel tabelaSen = (DefaultTableModel) tblSenioridades.getModel();
        
        tabelaSen.setNumRows(0);
        for (Senioridade s : listaSen)
        {
            Object[] obj = new Object[]{
            
            s.getCodigo(), s.getNivel(), duasCasas(s.getPorcentagem_rec()*100)};
            
            tabelaSen.addRow(obj);
        }
    }
     
     /**
     * Faz a conversao de um valor float para decimal format com duas casas decimais
     * @param var - valor para conversao
     * @return valor convertido
     */
    public String duasCasas(float var){
        DecimalFormat df = new DecimalFormat("0.00");
        return df.format(var);
    }
     
